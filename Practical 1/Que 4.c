#include <stdio.h>
int main()
{
    //Variable declaration
    int n1,n2,total;

    //Inputs
    printf("Enter number 1: ");
    scanf("%d",&n1);

    printf("Enter number 2: ");
    scanf("%d",&n2);

    //Process
    total=n1+n2;

    //Output
    printf("Total of the two numbers: %d",total);

    return 0;
}
